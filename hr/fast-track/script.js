var bac8=document.getElementById("qOneFastTrack")
var hide8=document.getElementById("aOneFastTrack")
bac8.style.backgroundColor="rgb(208, 226, 255)";
bac8.addEventListener("click", function(){
var color=bac8.style.backgroundColor;
switch(color) {
    case "rgb(208, 226, 255)":
        bac8.style.backgroundColor="white";
        hide8.style.display="block";
        break;
    case "white":
        bac8.style.backgroundColor="rgb(208, 226, 255)";
        hide8.style.display="none";
        break;
    default:        
} 
});
